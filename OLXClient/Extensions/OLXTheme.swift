import Foundation
import UIKit

extension UIColor {
    class func navigationBarColor() -> UIColor {
        return UIColor(red: 2/255.0, green: 184/255.0, blue: 117/255.0, alpha: 1.0)
    }
    
    class func cellBorderColor() -> UIColor {
        return UIColor(red: 220/255.0, green: 220/255.0, blue: 220/255.0, alpha: 1.0)
    }
    
    class func fadeColor() -> UIColor {
        return UIColor(red: 0/255.0, green: 0/255.0, blue: 0/255.0, alpha: 0.5)
    }

}
